package megabytesme.minelights.effects;

public class RGBColorDto {
    public int r, g, b;

    public RGBColorDto() {
        this.r = 0;
        this.g = 0;
        this.b = 0;
    }

    public RGBColorDto(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }
}